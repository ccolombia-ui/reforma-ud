/**
 * Build Vault Index — v8g-l5
 *
 * Lee archivos _Index_of_*.md (Zoottelkeeper) que no tienen frontmatter
 * y genera un módulo TypeScript para consumo del Client Component.
 * Corre entre velite build y next build.
 */

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';

const vaultDir = join(process.cwd(), 'content', 'vault-index');
const outDir = join(process.cwd(), 'src', 'lib', 'layout');
const outFile = join(outDir, 'vault-index-raw.ts');

let files = [];
try {
  files = readdirSync(vaultDir).filter(
    (f) => f.startsWith('_Index_of_') && f.endsWith('.md'),
  );
} catch (e) {
  console.warn('[build-vault-index] vault-index/ no encontrado', e.message);
}

const indices = files.map((f) => {
  const raw = readFileSync(join(vaultDir, f), 'utf-8');
  return { slug: f.replace(/\.md$/, ''), raw };
});

const tsContent = `/**
 * Auto-generated by scripts/build-vault-index.mjs
 * Do not edit manually.
 */
export const vaultIndexRaw = ${JSON.stringify(indices, null, 2)} as const;
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, tsContent);
console.log(`[build-vault-index] ${indices.length} índices → ${outFile}`);
