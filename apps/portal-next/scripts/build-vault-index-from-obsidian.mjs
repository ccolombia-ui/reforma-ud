/**
 * Build Vault Index from Obsidian — v8g-l5.2
 *
 * Escanea el vault REAL de Obsidian (H:\...) y genera un árbol de carpetas
 * tipo Obsidian File Explorer con nombres literales del vault.
 *
 * Incluye solo carpetas y archivos cuyo nombre comience con carácter
 * alfanumérico (excluye los que empiezan con '_' o '.').
 */

import { readdirSync, statSync } from 'node:fs';
import { join } from 'node:path';
import { writeFileSync, mkdirSync } from 'node:fs';

// Ruta del vault — misma que sync-vault.mjs
const VAULT_PATH = process.env.VAULT_PATH ||
  'H:\\.shortcut-targets-by-id\\1ondN7t4ewb2w-aN6iNesoT8yJGVqKpR2\\daath-zen\\R002-daath-cortex\\20--udfjc-reforma-vinculante';

const OUT_DIR = join(process.cwd(), 'src', 'lib', 'layout');
const OUT_FILE = join(OUT_DIR, 'vault-index-raw.ts');

function isAlnumFirst(name) {
  if (!name || name.length === 0) return false;
  const first = name[0];
  return /[a-zA-Z0-9]/.test(first);
}

function isMdFile(name) {
  const ext = name.split('.').pop()?.toLowerCase();
  return ext === 'md' || ext === 'mdx';
}

function toHref(filePath) {
  const withoutExt = filePath.replace(/\.(md|mdx)$/i, '');
  return '/' + withoutExt.replace(/\\/g, '/');
}

function toSlug(filePath) {
  return filePath.replace(/\.(md|mdx)$/i, '').replace(/\\/g, '/');
}

function buildTree(dirPath, basePath = '') {
  const entries = readdirSync(dirPath);
  const children = [];

  for (const name of entries) {
    if (!isAlnumFirst(name)) continue;

    const fullPath = join(dirPath, name);
    const relPath = basePath ? `${basePath}/${name}` : name;
    let stats;
    try {
      stats = statSync(fullPath);
    } catch {
      continue; // skip inaccessible
    }

    if (stats.isDirectory()) {
      const folderChildren = buildTree(fullPath, relPath);
      if (folderChildren.length > 0) {
        children.push({
          type: 'folder',
          id: relPath,
          name,
          slug: relPath,
          children: folderChildren,
        });
      }
    } else if (isMdFile(name)) {
      children.push({
        type: 'file',
        id: relPath,
        name: name.replace(/\.(md|mdx)$/i, ''),
        slug: toSlug(relPath),
        href: toHref(relPath),
      });
    }
  }

  // Ordenar: carpetas primero, luego archivos; cada grupo alfabéticamente
  children.sort((a, b) => {
    if (a.type === b.type) return a.name.localeCompare(b.name);
    return a.type === 'folder' ? -1 : 1;
  });

  return children;
}

// ── build ──────────────────────────────────────────────────────────────────
console.log(`[build-vault-index-from-obsidian] scanning: ${VAULT_PATH}`);

const tree = buildTree(VAULT_PATH, '');

const tsContent = `/**
 * Auto-generated by scripts/build-vault-index-from-obsidian.mjs (v8g-l5.2)
 * Do not edit manually.
 */
export interface VaultTreeNode {
  type: 'folder' | 'file';
  id: string;
  name: string;
  slug: string;
  href?: string;
  children: VaultTreeNode[];
}

export const vaultTreeRoot: VaultTreeNode[] = ${JSON.stringify(tree, null, 2)};
`;

mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(OUT_FILE, tsContent);
console.log(`[build-vault-index-from-obsidian] ${tree.length} roots → ${OUT_FILE}`);
